import * as React from "react";
import { cva, type VariantProps } from "class-variance-authority";
import { cn } from "@/lib/utils/cn";

const badgeVariants = cva(
  "inline-flex items-center rounded-full px-3 py-1 text-xs font-semibold transition-colors",
  {
    variants: {
      variant: {
        default: "bg-gold/10 text-gold-dark",
        seeds: "bg-seeds-light text-seeds-dark",
        sprouts: "bg-sprouts-light text-sprouts-dark",
        branches: "bg-branches-light text-branches-dark",
        roots: "bg-roots-light text-roots-dark",
        outline: "border border-bark/20 text-bark/70",
      },
    },
    defaultVariants: {
      variant: "default",
    },
  }
);

export interface BadgeProps
  extends React.HTMLAttributes<HTMLDivElement>,
    VariantProps<typeof badgeVariants> {}

function Badge({ className, variant, ...props }: BadgeProps) {
  return <div className={cn(badgeVariants({ variant }), className)} {...props} />;
}

export { Badge, badgeVariants };
